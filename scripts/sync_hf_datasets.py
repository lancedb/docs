"""
Sync Lance dataset cards from `lance-format/lance-huggingface` into Mintlify pages.

For each entry in `scripts/hf_datasets.yaml`, this script:

  1. Fetches `HF_DATASET_CARD.md` from the GitHub raw URL.
  2. Transforms it into MDX (rewrites frontmatter, strips the H1, injects a
     "View on Hugging Face" card).
  3. Writes `docs/datasets/<slug>.mdx`.

It also keeps `docs/docs.json` and the auto-generated card grid in
`docs/datasets/index.mdx` in sync with the same config file, so adding a new
dataset is a single-line edit to `hf_datasets.yaml` plus `make hf-sync`.

Hard-fails on any fetch error or unexpected card structure — partial
regeneration would be worse than a clear failure.
"""

from __future__ import annotations

import argparse
import json
import re
import sys
import urllib.error
import urllib.request
from dataclasses import dataclass
from pathlib import Path
from typing import Iterable

import yaml

REPO_ROOT = Path(__file__).resolve().parent.parent
CONFIG_PATH = REPO_ROOT / "scripts" / "hf_datasets.yaml"
DATASETS_DIR = REPO_ROOT / "docs" / "datasets"
INDEX_PATH = DATASETS_DIR / "index.mdx"
DOCS_JSON_PATH = REPO_ROOT / "docs" / "docs.json"

RAW_URL = "https://raw.githubusercontent.com/lance-format/lance-huggingface/main/{dir}/HF_DATASET_CARD.md"
HF_URL = "https://huggingface.co/datasets/lance-format/{hf}"

SYNC_START = "{/* HF_SYNC:START — content between these markers is generated by `make hf-sync`. Do not edit by hand. */}"
SYNC_END = "{/* HF_SYNC:END */}"

DESCRIPTION_MAX_CHARS = 250


@dataclass(frozen=True)
class Dataset:
    dir: str
    slug: str
    hf: str
    title: str
    category: str


@dataclass(frozen=True)
class Category:
    name: str
    datasets: tuple[Dataset, ...]


# ----- config loading ---------------------------------------------------------


def load_config() -> list[Category]:
    with CONFIG_PATH.open() as f:
        raw = yaml.safe_load(f)
    categories: list[Category] = []
    for cat in raw["categories"]:
        datasets = tuple(
            Dataset(
                dir=d["dir"],
                slug=d["slug"],
                hf=d["hf"],
                title=d["title"],
                category=cat["name"],
            )
            for d in cat["datasets"]
        )
        categories.append(Category(name=cat["name"], datasets=datasets))
    return categories


# ----- fetching ---------------------------------------------------------------


def fetch_card(ds: Dataset) -> str:
    url = RAW_URL.format(dir=ds.dir)
    try:
        with urllib.request.urlopen(url, timeout=30) as resp:
            if resp.status != 200:
                raise RuntimeError(f"{ds.slug}: HTTP {resp.status} from {url}")
            return resp.read().decode("utf-8")
    except urllib.error.HTTPError as exc:
        raise RuntimeError(f"{ds.slug}: HTTP {exc.code} from {url}") from exc
    except urllib.error.URLError as exc:
        raise RuntimeError(f"{ds.slug}: network error fetching {url}: {exc}") from exc


# ----- card transform ---------------------------------------------------------


FRONTMATTER_RE = re.compile(r"\A---\n(.*?)\n---\n?", re.DOTALL)
H1_RE = re.compile(r"^# .+?\n", re.MULTILINE)
LINK_RE = re.compile(r"\[([^\]]+)\]\([^)]+\)")
INLINE_CODE_RE = re.compile(r"`([^`]+)`")
BOLD_ITALIC_RE = re.compile(r"\*+([^*]+)\*+")
BARE_URL_RE = re.compile(r"https?://\S+")
WHITESPACE_RE = re.compile(r"\s+")
BIBTEX_OPEN_RE = re.compile(r"^@\w+\{")
EMPTY_ANGLE_RE = re.compile(r"<>")


def split_frontmatter(text: str) -> tuple[dict, str]:
    """Return (parsed YAML frontmatter, body without the frontmatter block)."""
    m = FRONTMATTER_RE.match(text)
    if not m:
        return {}, text
    fm = yaml.safe_load(m.group(1)) or {}
    body = text[m.end() :]
    return fm, body


def first_paragraph(body: str) -> str:
    """Return the first non-empty prose paragraph, skipping headers and code fences."""
    in_code = False
    para: list[str] = []
    for line in body.splitlines():
        stripped = line.strip()
        if stripped.startswith("```"):
            in_code = not in_code
            if para:
                break
            continue
        if in_code:
            continue
        if not stripped:
            if para:
                break
            continue
        if stripped.startswith("#"):
            continue
        para.append(stripped)
    return " ".join(para)


def plain_text(s: str) -> str:
    """Strip markdown formatting + URLs to a clean prose string."""
    s = LINK_RE.sub(r"\1", s)
    s = BARE_URL_RE.sub("", s)
    s = INLINE_CODE_RE.sub(r"\1", s)
    s = BOLD_ITALIC_RE.sub(r"\1", s)
    s = WHITESPACE_RE.sub(" ", s).strip()
    return s


def truncate(s: str, limit: int) -> str:
    if len(s) <= limit:
        return s
    cut = s[: limit - 1].rsplit(" ", 1)[0]
    return cut.rstrip(",.;:") + "…"


def sanitize_for_mdx(body: str) -> str:
    """Defensive fixes for known MDX hazards in upstream cards.

    Walks line-by-line so transforms apply only to prose, not code.

    - Wraps orphan bibtex entries (lines starting with `@article{`, `@inproceedings{`,
      etc. outside a fenced block) in a ```bibtex fence. MDX otherwise parses
      `{...}` as a JSX expression and rejects unbalanced citation contents.
    - Escapes the empty JSX-fragment literal `<>` (e.g. `Lance<>HF` in prose)
      that MDX treats as an unclosed React fragment.
    """
    lines = body.splitlines()
    out: list[str] = []
    in_fence = False
    i = 0
    while i < len(lines):
        line = lines[i]
        stripped = line.lstrip()
        if stripped.startswith("```"):
            in_fence = not in_fence
            out.append(line)
            i += 1
            continue
        if not in_fence and BIBTEX_OPEN_RE.match(stripped):
            depth = 0
            j = i
            while j < len(lines):
                depth += lines[j].count("{") - lines[j].count("}")
                if depth <= 0:
                    break
                j += 1
            out.append("```bibtex")
            out.extend(lines[i : j + 1])
            out.append("```")
            i = j + 1
            continue
        if not in_fence:
            # Escape `<>` outside inline code spans.
            parts = line.split("`")
            for k in range(0, len(parts), 2):
                parts[k] = EMPTY_ANGLE_RE.sub("&lt;&gt;", parts[k])
            line = "`".join(parts)
        out.append(line)
        i += 1
    return "\n".join(out)


def transform_card(ds: Dataset, card_text: str) -> tuple[str, str]:
    """Convert an upstream HF dataset card to a Mintlify-ready MDX page.

    Returns (mdx_page_content, description) — the description is reused by
    the landing-page card grid so summaries stay in sync.
    """
    _, body = split_frontmatter(card_text)
    body = H1_RE.sub("", body, count=1).lstrip()

    description = truncate(plain_text(first_paragraph(body)), DESCRIPTION_MAX_CHARS)
    body = sanitize_for_mdx(body)
    hf_url = HF_URL.format(hf=ds.hf)

    frontmatter = (
        "---\n"
        f'title: "{ds.title}"\n'
        f'sidebarTitle: "{ds.title}"\n'
        f'description: "{description}"\n'
        "---\n\n"
    )
    hf_card = (
        f'<Card\n'
        f'  title="View on Hugging Face"\n'
        f'  icon="/static/assets/logo/huggingface-logo.svg"\n'
        f'  href="{hf_url}"\n'
        f">\n"
        f"Source dataset card and downloadable files for `lance-format/{ds.hf}`.\n"
        f"</Card>\n\n"
    )
    return frontmatter + hf_card + body.rstrip() + "\n", description


# ----- index.mdx card grid ----------------------------------------------------


def render_card_grid(
    categories: Iterable[Category], descriptions: dict[str, str]
) -> str:
    parts: list[str] = []
    for cat in categories:
        parts.append(f"## {cat.name}\n")
        parts.append("<CardGroup cols={2}>")
        for ds in cat.datasets:
            desc = descriptions[ds.slug]
            parts.append(
                f'  <Card title="{ds.title}" href="/datasets/{ds.slug}">\n'
                f"    `lance-format/{ds.hf}` — {desc}\n"
                f"  </Card>"
            )
        parts.append("</CardGroup>\n")
    return "\n".join(parts)


def update_index_page(
    categories: list[Category], descriptions: dict[str, str]
) -> None:
    text = INDEX_PATH.read_text()
    start = text.find(SYNC_START)
    end = text.find(SYNC_END)
    if start == -1 or end == -1 or end < start:
        raise RuntimeError(
            f"Could not find HF_SYNC markers in {INDEX_PATH.relative_to(REPO_ROOT)}. "
            "Restore the start/end markers and re-run."
        )
    new_body = (
        text[: start + len(SYNC_START)]
        + "\n\n"
        + render_card_grid(categories, descriptions)
        + "\n"
        + text[end:]
    )
    INDEX_PATH.write_text(new_body)


# ----- docs.json --------------------------------------------------------------


def update_docs_json(categories: list[Category]) -> None:
    with DOCS_JSON_PATH.open() as f:
        docs = json.load(f)

    groups = [{"group": "Overview", "pages": ["datasets/index"]}]
    for cat in categories:
        groups.append(
            {
                "group": cat.name,
                "pages": [f"datasets/{ds.slug}" for ds in cat.datasets],
            }
        )

    tabs = docs["navigation"]["tabs"]
    for tab in tabs:
        if tab.get("tab") == "Datasets":
            tab["groups"] = groups
            break
    else:
        raise RuntimeError(
            "No 'Datasets' tab found in docs.json — add the tab scaffold before running sync."
        )

    DOCS_JSON_PATH.write_text(json.dumps(docs, indent=2) + "\n")


# ----- driver -----------------------------------------------------------------


def sync(dry_run: bool = False) -> None:
    categories = load_config()
    all_datasets = [ds for cat in categories for ds in cat.datasets]
    print(
        f"Syncing {len(all_datasets)} datasets across "
        f"{len(categories)} categories…"
    )

    DATASETS_DIR.mkdir(parents=True, exist_ok=True)

    config_slugs = {ds.slug for ds in all_datasets}
    existing_pages = {
        p.stem for p in DATASETS_DIR.glob("*.mdx") if p.stem != "index"
    }
    stale = existing_pages - config_slugs
    if stale:
        print(f"  Will remove stale pages: {sorted(stale)}")

    descriptions: dict[str, str] = {}
    for ds in all_datasets:
        print(f"  • {ds.category} / {ds.slug} ← {ds.dir}")
        card = fetch_card(ds)
        mdx, description = transform_card(ds, card)
        descriptions[ds.slug] = description
        out_path = DATASETS_DIR / f"{ds.slug}.mdx"
        if dry_run:
            print(f"    (dry run, would write {out_path.relative_to(REPO_ROOT)})")
        else:
            out_path.write_text(mdx)

    if not dry_run:
        for slug in stale:
            (DATASETS_DIR / f"{slug}.mdx").unlink()
        update_index_page(categories, descriptions)
        update_docs_json(categories)
        print(
            f"Wrote {len(all_datasets)} dataset pages, updated "
            f"{INDEX_PATH.relative_to(REPO_ROOT)} and "
            f"{DOCS_JSON_PATH.relative_to(REPO_ROOT)}."
        )


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="Fetch and transform, but do not write any files.",
    )
    args = parser.parse_args()
    try:
        sync(dry_run=args.dry_run)
    except RuntimeError as exc:
        print(f"error: {exc}", file=sys.stderr)
        return 1
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
